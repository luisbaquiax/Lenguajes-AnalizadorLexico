    private void llenarTabla() {
        DefaultTableModel model = (DefaultTableModel) tablaErrores.getModel();
        for (Token token : this.manejadorReportes.listConteoTipoTokens()) {
            String[] datos = {
                token.getTipo() + "",
                token.getVeces() + ""
            };
            model.addRow(datos);
        }
    }
